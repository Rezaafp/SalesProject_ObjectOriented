    private void cmdSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cmdSimpanMouseClicked
        // TODO add your handling code here:
        // TODO add your handling code here:
        String tKode=txtKode.getText();
        String tNama=txtNama.getText();
        String Alamat=txtAlamat.getText();
        String Kota=txtKota.getText();
        String Phone=txtTelp.getText();
        String Email=txtEmail.getText();
        try{
        if (edit==true){
        stm.executeUpdate("update pemasok set nm_pmk='"+tNama+"',alm_pmk='"+Alamat+"',kota_pmk='"+Kota+"',phone="+Phone+",email='"+Email+"' where kd_pmk='" + tKode + "'");
        }else{
        stm.executeUpdate("INSERT into pemasok VALUES('"+tKode+"','"+tNama+"','"+Alamat+"','"+Kota+"','"+Phone+"','"+Email+"')");
        }
        tblPmsk.setModel(new DefaultTableModel(dataTable,header));
        baca_data();
        aktif(false);
        setTombol(true);
        }catch(SQLException e) {
        JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_cmdSimpanMouseClicked

