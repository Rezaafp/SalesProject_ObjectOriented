//method baca data dari Mysql dimasukkan ke table pada form
private void baca_data(){
    try{
    stm = Con.createStatement();
    pstat = Con.prepareStatement("select * from pemasok",ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
    RsBrg = pstat.executeQuery();
    ResultSetMetaData meta = RsBrg.getMetaData();
    int col = meta.getColumnCount();
    int baris = 0;
    while(RsBrg.next()) {
    baris = RsBrg.getRow();
    }
    dataTable = new Object[baris][col];
    int x = 0;
    RsBrg.beforeFirst();
    while(RsBrg.next()) {
    dataTable[x][0] = RsBrg.getString("kd_pmk");
    dataTable[x][1] = RsBrg.getString("nm_pmk");
    dataTable[x][2] = RsBrg.getString("alm_pmk");
    dataTable[x][3] = RsBrg.getString("kota_pmk");
    dataTable[x][4] = RsBrg.getString("phone");
    dataTable[x][5] = RsBrg.getString("email");
    x++;
    }
    tblPmsk.setModel(new DefaultTableModel(dataTable,header));
    }
    catch(SQLException e)
    {
    JOptionPane.showMessageDialog(null, e);
    }
}

